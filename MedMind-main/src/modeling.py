"""
modeling.py
-----------
Leakage-free preprocessing pipeline, model definitions, nested
cross-validation for honest performance estimation, and final model
fitting for deployment.

WHY LEAKAGE MATTERS (read this before the code):
Leakage happens when information from outside the training fold "leaks"
into how the model is built for that fold -- most commonly, when you fit a
transform (imputer, scaler, encoder, resampler) on the WHOLE dataset and
only split into train/test afterward. If you compute e.g. the mean
cholesterol using every patient (including ones in your "test" set), then
impute missing test-set cholesterol values using that mean, the test set
has secretly influenced the model. Performance on that test set is no
longer an honest estimate of how the model will do on a brand-new patient,
because the test set wasn't really "unseen" -- its own statistics helped
build the imputer. The fix used throughout this project: EVERY step that
learns something from data (imputation values, scaling mean/std, one-hot
category list, class-balancing behaviour) lives inside a single
scikit-learn/imblearn Pipeline, and that pipeline is only ever `.fit()` on
a training fold; it is only ever `.transform()`-ed (never re-fit) on the
matching held-out fold. Cross-validation loops fit and evaluate a *fresh*
clone of the pipeline on every fold, so no fold's test data ever touches
that fold's preprocessing.
"""

