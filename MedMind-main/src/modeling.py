def build_preprocessor() -> ColumnTransformer:
    numeric_pipe_scaled = ImbPipeline([
        ("impute", SimpleImputer(strategy="median")),
        ("scale", StandardScaler()),
    ])
    categorical_pipe = ImbPipeline([
        ("impute", SimpleImputer(strategy="most_frequent")),
        ("onehot", OneHotEncoder(handle_unknown="ignore")),
    ])
    binary_pipe = SimpleImputer(strategy="most_frequent")

    preprocessor = ColumnTransformer(
        transformers=[
            ("num", numeric_pipe_scaled, NUMERIC_FEATURES),
            ("cat", categorical_pipe, CATEGORICAL_FEATURES),
            ("bin", binary_pipe, BINARY_FEATURES),
        ],
        remainder="drop",
    )
    return preprocessor


def build_lr_pipeline() -> ImbPipeline:
    return ImbPipeline([
        ("preprocess", build_preprocessor()),
        ("clf", LogisticRegression(
            class_weight="balanced", max_iter=2000, random_state=SEED
        )),
    ])


def build_xgb_pipeline() -> ImbPipeline:
    return ImbPipeline([
        ("preprocess", build_preprocessor()),
        ("clf", XGBClassifier(
            random_state=SEED, eval_metric="logloss",
        )),
    ])


